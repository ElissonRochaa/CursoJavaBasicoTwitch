public class PrimeiroProjeto {
    public static void main(String[] args){
        int a = 5;
        int b = 10;
        int c, d, e, f, g;
        boolean maior;
        

        //somar dois numeros
        a = a + b;
        d = a - b;
        e = a * b;
        f = b / a;
        g = b % a;
        /*
        > maior que
        < menor que
        >= maior ou igual que
        <= menor ou igual que
        == iguais
        != diferentes
        */
        maior = a != b;

        System.out.println(a);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(maior);
    }
}
